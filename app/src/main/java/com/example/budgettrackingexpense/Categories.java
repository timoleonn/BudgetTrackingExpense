package com.example.budgettrackingexpense;

public class Categories {
    private String Name;
    private Double Budget;

    public Categories() {
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public Double getBudget() {
        return Budget;
    }

    public void setBudget(Double budget) {
        Budget = budget;
    }
}
